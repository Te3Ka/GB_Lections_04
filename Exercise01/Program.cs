void PrintArray (int[,] printMatrix)
{
    for(int i = 0; i < printMatrix.GetLength(0); i++)
    {
        for(int j = 0; j < printMatrix.GetLength(1); j++)
        {
            if (printMatrix[i, j] == 0)
                Console.Write($"  ");
            else if (printMatrix[i, j] == 1)
                Console.Write($"X ");
            else
                Console.Write($"0 ");
        }
        Console.WriteLine();
    }
}

